#include<stdio.h>
#include<string.h>
#define MAX 1000

char m[MAX],n[MAX];

void main()
{
	int i,a,b,t;

	while(scanf("%s %s",m,n)==2)
	{
		if(m[0]=='0' && n[0]=='0') break;

		if(strcmp(n,"0")==0) printf("1\n");
		else 
		{
			a=m[strlen(m)-1]-'0';
			b=n[strlen(n)-1]-'0';
			b+=(n[strlen(n)-2]-'0')*10;

			b=b%4;
			if(b==0) b=4;

			t=1;
			for(i=1; i<=b; i++)
			{
				t*=a;
				t=t%10;
			}

			printf("%d\n",t);
		}
	}
}

